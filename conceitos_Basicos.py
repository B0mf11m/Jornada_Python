#=============================VARIÁVEIS=============================
"""
Variáveis: um espaço no computador para armazenar um dado, para ficar mais fácil o entendimento podemos fazer algumas analogias. como um item de sua gaveta de meias (por exemplo sua meia banca com linha vermelha), ou uma lista de compra. 

Tipos: inteiros(int); decimais(float); texto(str); lógica(boo)

vamos trazer  o conceito da lista de compra em um supermercado. cada variável é uim item, e cada item pode carregar um tipo de variável 
"""
pão = 6 # Aqui temos uma variável padrão com um valor numérico, este é um numero Inteiro (int); veja {pão} é o nome da variável e o {6} é o valor dela (para a lista dizemos para levar 6 pães) para o computador dizemos que pão é igual a o valor numérico {6}
carne=1.5 # Aqui temos uma variável padrão com um valor numérico, este é um numero Decimal (float); {carne} é  o nome da variável e seu valor é {1.5} (para a lista dizemos para levar 1.5kg de carne) para o computador estamos dizendo que carne é igual a {1.5}
marca = 'Pão puma' # Aqui temos uma variável padrão com um Texto (str); {marca} é o nome da variável e {"Pão puma"} é o valor (para a lista dizemos que queremos a marca pão puma) para o computador estamos passando o texto {"Pão puma"}
compraFeita = True # Aqui temos uma variável do tipo lógica  (ou seja com um valor verdadeiro ou falso ) (bool); {compraFeita} é o nome da variável e {True} ou seja verdade é seu valor (para a lista mar amos um check ) para o computador passamos o valor {True}

print("")# Comando {print}, utilizado para exibir mensagens no terminal de comando, tudo que estiver entre ("") será exibido
print('tipos de variáveis') #exibe a mensagem "tipos de variáveis"
print(type(pão)) #exibe no terminal o Tipo da variável {valor}
print(type(carne)) #exibe no terminal o Tipo da variável {valor2}
print(type(marca)) #exibe no terminal o Tipo da variável {nome}
print(type(compraFeita)) #exibe no terminal o Tipo da variável {tipo}

